package main;

public class MathUtil {
	
	public int add(int a, int b) {
		return a + b;
		
	}

	public int subtract(int a, int b) {
		return a - b;
	}

	public int devide(int a, int b) {
		return a / b;
	}
	
	public int mutiply(int a, int b) {
		return a * b;
	}
	
	public double testCircumference(double a) {
		return Math.PI * 2*a;
	}
}
